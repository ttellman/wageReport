private void runReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_runReportActionPerformed
          
    //Displays in text area report title
    outputField.append("Report of your wages to Date:");
    outputField.append("\n"); //start a new line
    //add total time and earnings and display in text field
    double totalTime = 0, averageWage = 0;
    for(int i = 0; i < index; i++){
        totalTime += earnings[i][0];
        averageWage += earnings[i][1];
    }
    averageWage = averageWage/index;
    outputField.append("TotalTime: " + totalTime + "   AverageWage: " + averageWage);
    outputField.append("\n"); 
    
    if (averageWage < 7.75)
    {
    outputField.append("Your average wages per hour are below average");
    }
    else if (averageWage >= 7.75 && averageWage <= 7.75*2)
    {
    outputField.append("Your average wages per hour are then average");
    }
    else
    {
    outputField.append("Your average wages per hour are above average");
    }
    

}//GEN-LAST:event_runReportActionPerformed

