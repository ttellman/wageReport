private void enterDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterDataActionPerformed
// exception for minutes entered less than or equal to 0 and not greater than 240
    double tutorTime = 0;

    try {
        tutorTime = Double.parseDouble(
                this.tutorTime.getText());
        if (tutorTime <= 0 || tutorTime > 240) {
            throw new Exception();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid input. Minutes can't be less than 0 or more than 240. Please try again",
                "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    double paymentEarned = 0;
    try {
        paymentEarned = Double.parseDouble(
                this.paymentEarned.getText());

        if (paymentEarned <= 0) {
            throw new Exception();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid input. Earnings must be greater than zero. Please try again", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    // output from array displaying minutes and earnings. 
    if (index < 10) {
        earnings[index][0] = tutorTime;
        earnings[index][1] = paymentEarned;
        outputField.append("\n Minutes Earnings\n ***************************************");
        outputField.append("\n");
        outputField.append("Minutes: " + earnings[index][0] + "         ");
        outputField.append("Earnings: " + earnings[index][1] + "\n");
        index++;
        this.tutorTime.setText("");
        this.paymentEarned.setText("");
    } else {
        outputField.setText("You have exceeded the number of entries allowed (10).");
    }

// separates detail from report and lists all minutes and earnings in array

}//GEN-LAST:event_enterDataActionPerformed

